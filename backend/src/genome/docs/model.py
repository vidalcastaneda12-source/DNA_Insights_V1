@dataclass(frozen=True, slots=True)
class Frontmatter:
    """The parsed ``---``-fenced block atop a finding. Authoritative for the
    supersession edge (plan ¬ß0 resolution #4)."""

    type: str
    """One of :data:`FINDING_TYPE_VOCAB`."""
    status: str
    """One of :data:`STATUS_VOCAB`."""
    actors: tuple[str, ...]
    """Canonical actor names (post legacy-map)."""
    date: str
    """ISO-8601 date (``YYYY-MM-DD``)."""
    supersedes: tuple[str, ...] = ()
    """finding-id and/or DEC-id this finding's decision replaces."""
    superseded_by: tuple[str, ...] = ()
    """finding-id and/or DEC-id that replaces this finding's decision."""


@dataclass(frozen=True, slots=True)
class LedgerRow:
    """One row of the ``MEMORY.md`` decision table. Content columns are immutable
    once written; only ``status``/``superseded_by`` transition (insert-then-flip)."""

    dec: str
    """``DEC-NNNN`` ‚Äî unique, monotonic."""
    kind: str
    """One of :data:`KIND_VOCAB`."""
    date: str
    """ISO-8601 date."""
    status: str
    """One of :data:`STATUS_VOCAB`."""
    actors: tuple[str, ...]
    """Canonical actor names."""
    provenance: str
    """Verbatim source (``PR #93``, ``git:<sha>``) or the literal ``unknown``."""
    decision: str
    """Free text; raw ``|`` is escaped. Anchors are *referenced*, never copied."""
    detail_link: str
    """Path/anchor to the canonical detail (a finding, a CLAUDE.md obs #N)."""
    superseded_by: str | None = None
    """DEC-id of the superseding decision; required when status is
    ``superseded``/``reversed``, ``None`` otherwise."""


@dataclass(frozen=True, slots=True)
class CheckViolation:
    """A single hard-fail emitted by :func:`genome.docs.validator.check`."""

    dimension: str
    """``capture`` | ``retrieval`` | ``lifecycle``."""
    code: str
    """Stable machine code, e.g. ``MISSING_FRONTMATTER``, ``ORPHAN_SUPERSESSION``."""
    location: str
    """Where it lives ‚Äî a file path + line, or a DEC id."""
    message: str
    """Human-readable explanation printed by ``genome docs check``."""


@dataclass(frozen=True, slots=True)
class CheckReport:
    """The outcome of a ``genome docs check`` run."""

    violations: tuple[CheckViolation, ...]

    @property
    def ok(self) -> bool:
        """True iff there are no violations (drives the CLI exit code)."""
        return not self.violations

    def by_dimension(self, dimension: str) -> tuple[CheckViolation, ...]:
        """All violations for one of CAPTURE / RETRIEVAL / LIFECYCLE."""
        return tuple(v for v in self.violations if v.dimension == dimension)


@dataclass(frozen=True, slots=True)
class IndexResult:
    """The outcome of a ``genome docs build-index`` run."""

    changed: bool
    """True when the rendered index differs (normalize-then-compare) from committed."""
    rendered: str
    """The full README text with the regenerated marker block."""
    findings_indexed: int
    """Number of findings folded into the index table."""
    cross_links_derived: int
    """Number of supersession cross-links derived from finding frontmatter."""
