"""Frozen vocabulary + data model for the decision ledger and finding frontmatter.

This module is the source of truth for the two closed axes, the actor enum, and the
violation-code vocabulary that ``genome docs check`` enforces. It is
**import-side-effect-free** and has **no** dependency on :mod:`genome.db` or any database
driver (plan ¬ß3 / Task 3): ``python -c "import genome.docs.model"`` must not import DuckDB
or SQLCipher.

The two closed axes are orthogonal (plan ¬ß0 resolution #2):

* ``status`` ‚Äî the *lifecycle* axis (:data:`STATUS_VOCAB`): where a decision sits in its
  life. Transitions are append-then-flip, never an in-place content edit ‚Äî the intent of
  locked decision #7, relocated to the validator gate because a markdown ledger has no
  transaction.
* ``kind`` ‚Äî the *granularity* axis (:data:`KIND_VOCAB`), the former ``grain`` column.
  ``tactical`` lives here, **not** on the status axis, so a tactical decision still carries
  a full lifecycle ``status`` and can be recorded as superseded/reversed.

The violation codes live here (not in :mod:`genome.docs.validator`) so the parsers can tag
their errors with a code without importing the validator ‚Äî :mod:`genome.docs.validator`
re-exports them, so ``validator.DUPLICATE_DEC_ID`` still resolves.
"""

