"""Maps every violation code to its CAPTURE / RETRIEVAL / LIFECYCLE dimension."""


def canonicalize_actor(name: str) -> str | None:
    """Map a raw actor token to its canonical name, or ``None`` if unknown.

    Returns the input unchanged when already canonical; applies
    :data:`LEGACY_ACTOR_MAP` otherwise; returns ``None`` for a token that is neither
    canonical nor mapped (the validator turns ``None`` into a hard fail).
    """
    if name in CANONICAL_ACTORS:
        return name
    return LEGACY_ACTOR_MAP.get(name)


